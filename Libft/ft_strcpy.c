#include "libft.h"

char *ft_strcpy(char *dst, char *src)
{
	int	i;

	if (!src)
		return (NULL);
	i = -1;
	while (src[++i])
		dst[i] = src[i];
	dst[i] = 0;
	return (dst);
}
